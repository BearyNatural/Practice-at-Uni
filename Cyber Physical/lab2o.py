# Sum of all number in the list using a for loop

numbers = [3, 9, 2, 11, 17, 16, 21, 8, 36, 1, 5, 2]

# Initialize a variable to store the sum
total = 0

# Iterate over each number in the list and add it to the total
for num in numbers:
    total += num

